package com.github.ahnshy.weblotto.model;

public class SearchParam {
    private int round;
    private int index;
    private int page;

    public int getRound() {
        return round;
    }

    public void setRound(int r) {
        this.round = r;
    }

    public int getIndex() {
        return index;
    }

    public void setIndex(int i) {
        this.index = i;
    }

    public int getPage() {
        return page;
    }

    public void setPage(int page) {
        this.page = page;
    }
}
